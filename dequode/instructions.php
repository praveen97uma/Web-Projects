<h2>General Instructions</h2>
<p>
1. Deqoude V2 is an online crypt/puzzle event of Thomso '10. Proceed through the levels by answering each question currectly. The first one to finish all levels wins. It is requested to choose a respectable username/nickname while playing.
<br/><br/>
2. Your current position will be given in the leaderboard. If more than one person is on the same level, the person who has reached the level first will have his/her name displayed higher in the leaderboard than others.
<br/><br/>
3. Being an online event, you can search for and about the clues given using internet sources like Google, Wikipedia, Google images etc. But remember, only searching the net for clues won't help you much, as several aspects of many levels rely purely on mental deductions and reasoning.
<br/><br/>
4. Other sources for clues include viewing the page source code (Ctrl + U in Firefox), the title of the levels' pages and their URLs etc. Some levels may require changes in the URL based on the clues you get.
<br/><br/>
5. Answer of each level is a string of english alphabets. No capitals i.e uppercase letters and spaces are allowed. Also no numbers or special characters like _,&,# etc are allowed. For eg, if the answer you find is 'Book 47', then type 'bookfortyseven' as the answer.
<br/><br/>
6. Whenever you are stuck on a level, you can ask for hints on the FORUM. The forum will be active throughout the event.
<br/><br/>
7. Remember, your IP address is being recorded. Hence, your activity is being continously monitored.
</p>

<h2>Code of Conduct on the Board of Forums</h2>
<p>
1. Forums are meant for healthy discussions. Please post relevant queries in parliamentary language.
<br/><br/>
2. For just viewing the posts by others on the forums, you don't have to register on the Board. But for posting your queries regarding various levels, you must register (Recommended). Registration on the Board will be open once the event commences. It is requested to choose a respectable username/nickname while registering.
<br/><br/>
3. Forum moderators will post hints regarding any particular level at a time they think fit (usually this will be when there are many people stuck on that level) .
<br/><br/>
3. Continous begging for hints, pestering or cursing will have a negative effect.
<br/><br/>
4. NO SPOILERS PLEASE. If you have solved a particular level, you are welcome to post hints for others regarding the same, but they shouldn't be too explicit.
<br/><br/>
5. The Board of Forums is being continously monitored. So maintain your decorum.
</p>

